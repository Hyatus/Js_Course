
// Object Literal
const producto = {
    nombre: "Monitor 20 pulgadas",
    precio: 300,
    disponible: true
  };


console.log(Object.keys(producto)); // Retorna un array con los keys del objeto 


console.log(Object.values(producto)); // Retorna un array con los valores de sus keys

console.log(Object.entries(producto)) // Devuelve en pares claves y valores 

